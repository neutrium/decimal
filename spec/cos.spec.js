var Decimal = require('../dist/Decimal').Decimal;

function test(cases)
{
    for(var i = 0; i < cases.length; i++)
    {
        var testCase = cases[i];

        it('should cos ' + testCase[0] + ' equal ' + testCase[3], function() {
            Decimal.precision = testCase[1];
            Decimal.rounding = testCase[2];
            expect(new Decimal(testCase[0]).cos().valueOf()).toEqual(testCase[3]);
        });
    }

    return true;
}

