var Decimal = require('../dist/Decimal').Decimal;

function test(cases)
{
    for(var i = 0; i < cases.length; i++)
    {
        var testCase = cases[i];

        it('should negative ' + testCase[1] + ' equal ' + testCase[0], function() {
            expect(new Decimal(testCase[1]).neg().valueOf()).toEqual(testCase[0]);
        });
    }
    return true;
}

